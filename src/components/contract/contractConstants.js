// 금액 한글 변환
export const priceToKorean = (price) => {
  if (!price || price === 0) return '영원';
  const units = ['', '일', '이', '삼', '사', '오', '육', '칠', '팔', '구'];
  const bigUnits = ['', '만', '억'];
  let result = '';
  let num = price;
  let bigIdx = 0;
  while (num > 0) {
    const chunk = num % 10000;
    if (chunk > 0) {
      let chunkStr = '';
      const t = Math.floor(chunk / 1000), h = Math.floor((chunk % 1000) / 100);
      const ten = Math.floor((chunk % 100) / 10), one = chunk % 10;
      if (t > 0) chunkStr += (t === 1 ? '' : units[t]) + '천';
      if (h > 0) chunkStr += (h === 1 ? '' : units[h]) + '백';
      if (ten > 0) chunkStr += (ten === 1 ? '' : units[ten]) + '십';
      if (one > 0) chunkStr += units[one];
      result = chunkStr + bigUnits[bigIdx] + result;
    }
    num = Math.floor(num / 10000);
    bigIdx++;
  }
  return result + '원정';
};
